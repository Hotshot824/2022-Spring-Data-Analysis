import plotly.express as px

data = dict(number=[39,27.4,20.6,11,2],
            stage= [" Visit", "Downloads", "Potential", "Price", "Invoice"])

fig = px.funnel(
          data,
          x='number',
          y='stage',
          )

fig.show()

